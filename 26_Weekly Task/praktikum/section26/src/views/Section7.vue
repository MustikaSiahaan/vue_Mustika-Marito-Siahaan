Grid and Container pada Bootstrap
Grid
Grid digunakan untuk mengatur tata letak dan ukuran dengan dua belas kolom, lima tingkat responsif default, Sass variabel dan mixin ,dan berbagain kelas yang telah ditentukan sebelumnya.

Menggunakan .container untuk lebar piksel yang responsif atau .container-fluid untuk lebar: 100% di semua ukuran perangkat.
Grid kolom tanpa lebar yang ditentukan disusun otomatis sebagai kolom dengan lebar yang sama. Misalnya, empat kolom dengan .col-sm otomatis memiliki lebar 25%.
Class prefix : .col-xs-(phone); .col-sm-(tablets); .col-md-(desktop medium); .col-lg-(desktop large)
Container
Container merupakan elemen tata letak yang diperlukan saat menggunakan sistem grid. Menggunakan fixed-width yang responsif dimana lebar maksimumnya berubah di setiap breakpoint dan flui-widthnya 100% sepanjang waktu

